UPDATE MPM_BAIXADOS_ANALITICO mba
SET DATA_DE_AUTUACAO = (
    SELECT MAX(efp.DATA_AUTUACAO) -- Use MAX or another aggregate function
    FROM ejud_fac_processo efp
    WHERE efp.NUM_PROCESSO = mba.CNJ
)
WHERE EXISTS (
    SELECT 1
    FROM ejud_fac_processo efp
    WHERE efp.NUM_PROCESSO = mba.CNJ
);

